	Id			bigint IDENTITY(1,1) NOT NULL PRIMARY KEY,
	FirstName	nvarchar(100),
	LastName	nvarchar(100),
	Age         int,
	InsertDate	datetime2 NOT NULL DEFAULT getdate()
)
GO
